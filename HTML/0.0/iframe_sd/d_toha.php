          <h1>童話とは</h1>
          <p>童話とは、児童が読む、または親などの大人が幼年児童に読み聞かせる子ども向けの、民話、伝説</p>
          <p>、神話、寓話、創作された物語等である。創作童話の多くは幼年、児童向けの短篇作品をさす。最</p>
          <p>も古い童話論は「童話は乳母のするおとぎ話である」というプラトンである。</p>
          <h3>日本における概念</h3>
          <p>江戸時代より学者や作家である山東京伝や曲亭馬琴らが「童話」と記して「むかしばなし」や「わ</p>
          <p>らべものがたり」と読ませていたように昔話を指す言葉であったため大正時代頃までは、高木敏雄</p>
          <p>の『童話の研究』などに代表されるように昔話研究などで「童話」という言葉が使われていた。一</p>
          <p>方、明治時代に巖谷小波は幼年児童向けの読み物に「お伽噺」という語を用いて出版やお話し会な</p>
          <p>どの活発な活動をおこなっていた。その後、1918年創刊の鈴木三重吉主宰『赤い鳥』の頃より「お</p>
          <p>伽噺」ではなく「童話」という呼称を使い出し、競合誌に『童話』も創刊されて一般に用いられる</p>
          <p>ようになった。アンデルセン童話の影響もあったと思われるが、『赤い鳥』でも当初は、昔話の意</p>
          <p>味合いだったが次第に創作童話も含むようになり、一般に子ども向け全般の文学に対して用いられ</p>
          <p>るようになり、これが現在でも引き継がれている。</p>
